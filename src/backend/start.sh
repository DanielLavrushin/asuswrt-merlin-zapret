start() {
    printlog true "Starting $ADDON_TITLE..." $CINFO

    printlog true "Starting $ADDON_TITLE... done" $CSUC
}

stop() {
    printlog true "Stopping $ADDON_TITLE..." $CINFO

    printlog true "Stopping $ADDON_TITLE... done" $CSUC
}

restart() {
    printlog true "Restarting $ADDON_TITLE..." $CINFO

    printlog true "Restarting $ADDON_TITLE... done" $CSUC
}
